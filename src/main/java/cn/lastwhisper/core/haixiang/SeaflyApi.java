	//获得加密字符串
	public static String getDesStr(String appKey, String sign) {
		try {
			DesUtils desUtils = new DesUtils(appKey); 
			return desUtils.encrypt(sign);
		} catch (Exception e) {
			e.printStackTrace();
		}
		return "";
	} 
