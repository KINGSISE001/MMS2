package cn.lastwhisper.modular.pojo;

public class o2oBrandInfo {
    private String appname;

    private Integer mtappid;

    private String mtappkey;

    private String brandName;

    private Integer brandId;

    public String getAppname() {
        return appname;
    }

    public void setAppname(String appname) {
        this.appname = appname;
    }

    public Integer getMtappid() {
        return mtappid;
    }

    public void setMtappid(Integer mtappid) {
        this.mtappid = mtappid;
    }

    public String getMtappkey() {
        return mtappkey;
    }

    public void setMtappkey(String mtappkey) {
        this.mtappkey = mtappkey;
    }

    public String getBrandName() {
        return brandName;
    }

    public void setBrandName(String brandName) {
        this.brandName = brandName;
    }

    public Integer getBrandId() {
        return brandId;
    }

    public void setBrandId(Integer brandId) {
        this.brandId = brandId;
    }
}